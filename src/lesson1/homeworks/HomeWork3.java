package lesson1.homeworks;

public class HomeWork3 {

    public static void main(String[] args) {
        int i = 10;
        int sum = 0;
        while (i < 56) {
            sum += i;
            i += 2;
        }
        System.out.println("Sum of even numbers from 10 to 55 =" + sum);


    }
}
